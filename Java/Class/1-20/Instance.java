class Instance {

    String demo;
    int num;
    char alp;

    public static void main(String args[]){
        
        Instance a = new Instance();

        int num = 1;
        String name = "Suman";
        char alp = 'l';

        System.out.println(a.demo);
        System.out.println(a.num);
        System.out.println(a.alp);

        System.out.println(name);
        System.out.println(num);
        System.out.print(alp);
    }
}